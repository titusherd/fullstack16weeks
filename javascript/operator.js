// cek persamaan
const isEqual = 1 == 1;
const isEqual2 = 3 == 2;
console.log(isEqual);
console.log(isEqual2);

// cek perbandingan
const isNotEqual = 1 != 1;
const isNotEqual2 = 1 != 4;
console.log(isNotEqual);
console.log(isNotEqual2);

// strict comparison
const isStrictEqual = 1 === 1;
const isStrictEqual2 = '1' === 1;
console.log(isStrictEqual);
console.log(isStrictEqual2);
